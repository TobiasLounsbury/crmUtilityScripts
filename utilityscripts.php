function getUtilityScripts() {
  $UtilityScripts = array();

  $extRoot = dirname(__FILE__) . DIRECTORY_SEPARATOR;
  $path = $extRoot.DIRECTORY_SEPARATOR."scripts".DIRECTORY_SEPARATOR."*.php";
  $files = glob($path);
  foreach ($files as $filename) {
    include $filename;
  }

  return $UtilityScripts;
}